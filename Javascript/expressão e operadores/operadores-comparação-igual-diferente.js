/*
# Operadores de comparação
- Ira comparar valores e retornor um boolean como resposta a comparação
- igualdade      
  - ==
  - != 
*/

let one = 1
let two = 2

// == igual a 
console.log(two == 1)
console.log(two == '1')

// != diferente de
console.log(one != two)
console.log(one != 1)
console.log(one != '1')
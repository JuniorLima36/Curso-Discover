/*
# Operadores de comparação: maior e menor   
  - >
  - => 
  - < 
  - <=
*/

let one = 1
let two = 2

// >  maior que 
console.log(one > two)

// >=  maior igual a  
console.log(one >= 1)
console.log(two >= 1)

// <  maior que 
console.log(one < two)

// <=  manor igual a  
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)

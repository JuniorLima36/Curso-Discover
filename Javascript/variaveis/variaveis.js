/*
# Tipos

O JS é uma linguagem fracamente tipada e dinamica
- variaveis não precisam ter um tipo previamente definido
- podemos mudar o conteudo da variavel

# Conhecendo variavies

- Nomes simbolicos para receber algum valor
- Atalhos de codigo
- Identificadores
- 3 palavras reservadas para criar uma variavel
  - var
  - let
  - const
*/

var clima = 'quente'
let hora = '12:00'
const fruta = 'banana'

console.log(clima)
console.log(hora)
console.log(fruta)